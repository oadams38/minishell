#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*s_dup;

	i = ft_strlen(s);
	s_dup = (char *) malloc(sizeof(char) * (i + 1));
	if (!s_dup)
		return (NULL);
	i = 0;
	while (s[i])
	{
		s_dup[i] = s[i];
		i++;
	}
	s_dup[i] = '\0';
	return (s_dup);
}
